<?php
session_start();

// Подключение к базе данных и другие настройки

if (isset($_POST['signin'])) {
    // Получаем данные из формы
    $username = $_POST['username'];
    $password = $_POST['password'];

    // Здесь следует добавить проверку данных и запрос в базу данных для проверки совпадения имени пользователя и пароля

    // Пример проверки (замените этот код на свой):
    if ($username == 'ваш_логин' && $password == 'ваш_пароль') {
        // Успешная авторизация
        $_SESSION['username'] = $username;
        header('Location: welcome.php'); // Перенаправление на страницу приветствия
        exit();
    } else {
        // Неправильные учетные данные
        $_SESSION['error'] = 'Неправильное имя пользователя или пароль';
        header('Location: login.php'); // Перенаправление обратно на страницу входа
        exit();
    }
}
?>